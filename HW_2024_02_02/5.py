# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –ø–æ–ª—É—á–∏—Ç—å –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Ü–µ–ª—É—é –∏ –¥—Ä–æ–±–Ω—É—é —á–∞—Å—Ç—å —á–∏—Å–ª–∞ –º–∏–ª—å –∑–∞ –¥–≤–∞ –≤–≤–æ–¥–∞: —Å–º. –ø—Ä–∏–º–µ—Ä –Ω–∏–∂–µ.
# –ü–æ—Å–ª–µ –ø—Ä–æ–≤–µ–¥–µ–Ω–∏—è –≤—ã—á–∏—Å–ª–µ–Ω–∏—è –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –≤—ã–≤–µ—Å—Ç–∏ —Å—Ç—Ä–æ–∫—É —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º–∏, –∫–∞–∫ –≤ –ø—Ä–∏–º–µ—Ä–µ –Ω–∏–∂–µ. –ó–Ω–∞—á–µ–Ω–∏–µ –∫–∏–ª–æ–º–µ—Ç—Ä–æ–≤ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–∏ –æ–∫—Ä—É–≥–ª–µ–Ω–æ –¥–æ –æ–¥–Ω–æ–≥–æ –∑–Ω–∞–∫–∞ –≤ –¥–µ—Å—è—Ç–∏—á–Ω–æ–π —á–∞—Å—Ç–∏.

# –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Å–ª–µ–¥—É—é—â–µ–µ —Å–æ–æ—Ç–Ω–æ—à–µ–Ω–∏–µ: 1 –º–∏–ª—è = 1.61 –∫–º

# –î–ª—è –æ–∫—Ä—É–≥–ª–µ–Ω–∏—è –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –≤—Å—Ç—Ä–æ–µ–Ω–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é round(). –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –µ—ë –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–∏ –≤ –¥–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏–∏:
#     https://docs.python.org/3/library/functions.html

# –¢–∞–∫–∂–µ, –¥–ª—è –æ–∫—Ä—É–≥–ª–µ–Ω–∏—è –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–∏–Ω—Ç–∞–∫—Å–∏—Å f-—Å—Ç—Ä–æ–∫:
#     >>> f'{12.358:.2f}'
#     '12.36'
#     >>> f'{12.358:.1f}'
#     '12.4'

# –ü—Ä–∏–º–µ—Ä –≤–≤–æ–¥–∞:
#     15
#     7

# –ü—Ä–∏–º–µ—Ä –≤—ã–≤–æ–¥–∞:
#     15.7 –º–∏–ª—å = 25.3 –∫–º

miles_whole = int(input("Enter the whole part of miles: "))
miles_fractional = int(input("Enter the fractional part of miles: "))

miles = miles_whole + miles_fractional / 10
kilometers = miles * 1.61

print(f"{miles_whole}.{miles_fractional} miles = {kilometers:.1f} km")

# –í–≤–æ–¥ –∏ –≤—ã–≤–æ–¥:
# Enter the whole part of miles: 15
# Enter the fractional part of miles: 7
# 15.7 miles = 25.3 km

